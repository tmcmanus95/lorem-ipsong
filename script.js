function getRandomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function createParagraphs() {
  if (numberInput.value <= 10) {
    checkGenre();
    resultsElement.textContent = "";
    let numberOfParagraphs = Number(numberInput.value);
    copyButtonContainer.textContent = "";
    resultsElement.textContent = " ";
    var copyButton = document.createElement("img");
    copyButton.id = "copy-button";
    copyButton.src = copyButtonSource;
    copyButtonContainer.style.backgroundColor = textBackground;
    copyButton.addEventListener("click", copyParagraphsToClipboard);

    copyButtonContainer.appendChild(copyButton);

    for (
      let paragraphIndex = 0;
      paragraphIndex < numberOfParagraphs;
      paragraphIndex++
    ) {
      makePElement();
    }
  } else if (numberInput.value > 10 && genreSelect.value !== "null") {
    var submitLessMessage = document.createElement("p");
    submitLessMessage.textContent =
      "Please request 10 or fewer paragraphs at a time";
    submitLessMessage.style.color = submitLessMessageText;
    submitLessMessage.id = "submit-fewer";
    inputContainer.appendChild(submitLessMessage);
    setTimeout(() => {
      inputContainer.removeChild(submitLessMessage);
    }, 2000);
  }
}

function makePElement() {
  checkGenre();
  numberOfSentences = getRandomInt(4, 8);
  const pElement = document.createElement("p");
  newSentenceArray = [];
  let sentenceIndex = 0;
  function loopThroughSentences() {
    if (sentenceIndex === 0) {
      var tabBreak = document.createElement("span");
      tabBreak.id = "tab-span";
      pElement.appendChild(tabBreak);
      generateNextSentence();
      sentenceIndex++;
      loopThroughSentences();
    } else if (sentenceIndex < numberOfSentences && sentenceIndex > 0) {
      generateNextSentence();
      sentenceIndex++;
      loopThroughSentences();
    } else if (sentenceIndex === numberOfSentences) {
      outputContainer.appendChild(resultsElement);
    }
  }

  function generateNextSentence() {
    lengthOfSentence = getRandomInt(6, 25);

    wordIndex = getRandomInt(0, genreChosen.length - 1);
    firstWord =
      genreChosen[wordIndex].charAt(0).toUpperCase() +
      genreChosen[wordIndex].slice(1);
    newSentenceArray.push(firstWord);

    for (i = 0; i < lengthOfSentence; i++) {
      wordIndex = getRandomInt(0, genreChosen.length - 1);
      newSentenceArray.push(genreChosen[wordIndex]);
    }

    var newSentenceText = newSentenceArray.join(" ");
    var sentence = document.createElement("span");
    sentence.textContent = newSentenceText;
    pElement.appendChild(sentence);

    var period = document.createElement("span");
    period.textContent = ". ";
    pElement.appendChild(period);

    newSentenceArray = [];
  }
  loopThroughSentences();
  resultsElement.appendChild(pElement);
  pElement.style.color = textColor;

  outputContainer.appendChild(resultsElement);
}

